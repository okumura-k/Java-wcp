
public class Chapter04 {
	public static void main(String[] args) {
		// 「String」型の変数「str」の宣言と初期化して出力
		String str = "okumura";
		System.out.println(str);

		// 変数「str」に再代入して出力
		str = "パンが無ければケーキを食べればいいじゃない";
		System.out.println(str);

		// 「int」型で宣言したクラス定数を出力
		final int LUCKY_NUMBER = 7;
		System.out.println(LUCKY_NUMBER);
	}
}
